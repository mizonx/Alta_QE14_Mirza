package OOP.Soal3;

public class Calc {
    public static int penjumlahan(int x, int y){
        return x+y;
    }
    public static int pengurangan(int x, int y){
        return x-y;
    }
    public static int perkalian(int x, int y){
        return x*y;
    }
    public static int pembagian(int x, int y){
        return x/y;
    }
}
